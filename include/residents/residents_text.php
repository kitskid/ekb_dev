План мероприятий по подготовке и проведению празднования юбилея Екатеринбурга был разработан в 2019
году и утверждён Губернатором Свердловской области Евгением Куйвашевым. За прошедшие несколько лет
уже реализованы целый ряд крупных инфраструктурных проектов, впереди — не менее амбициозные задачи.